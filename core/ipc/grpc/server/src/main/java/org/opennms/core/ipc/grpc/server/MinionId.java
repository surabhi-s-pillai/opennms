package org.opennms.core.ipc.grpc.server;

import com.google.common.base.Objects;

public class MinionId {

    private final String minionId;

    private final String location;

    public MinionId(String minionId, String location) {
        this.minionId = minionId;
        this.location = location;
    }

    public String getMinionId() {
        return minionId;
    }

    public String getLocation() {
        return location;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        MinionId minionId1 = (MinionId) o;
        return Objects.equal(minionId, minionId1.minionId) &&
                Objects.equal(location, minionId1.location);
    }

    @Override
    public int hashCode() {
        return Objects.hashCode(minionId, location);
    }
}
